def test_publish_clean_scaffold_releases_stages_assets_in_dry_run(tmp_path: Path) -> None:
    """The clean scaffold publisher should own release staging and gh calls."""

    outputs = tmp_path / "outputs"
    package = (
        outputs
        / "document-templates"
        / "dsw-science-europe"
        / "v1.30.1"
        / "zh-Hant"
        / "scaffold"
        / "dsw-science-europe-zh-hant-scaffold-1.30.1.zip"
    )
    package.parent.mkdir(parents=True)
    package.write_text("package\n", encoding="utf-8")
    workspace = outputs / "upstream-workspaces" / "dsw-science-europe" / "v1.30.1"
    workspace.mkdir(parents=True)
    (workspace / "upstream.json").write_text("{}\n", encoding="utf-8")
    preview = outputs / "project-render" / "dsw-science-europe" / "v1.30.1" / "zh-Hant" / "scaffold"
    preview.mkdir(parents=True)
    (preview / "test-project.pdf").write_text("pdf\n", encoding="utf-8")

    result = run(
        [
            sys.executable,
            str(PUBLISH_SCRIPT),
            "--outputs-root",
            str(outputs),
            "--release-root",
            str(outputs / "release-assets" / "clean-scaffold"),
            "--repository",
            "owner/repo",
            "--run-id",
            "123",
            "--commit-sha",
            "abc123",
            "--dry-run",
        ],
    )

    assert result.returncode == 0, result.stdout + result.stderr
    release_dir = outputs / "release-assets" / "clean-scaffold" / "v1.30.1"
    assert sorted(path.name for path in release_dir.iterdir()) == [
        "SHA256SUMS",
        "clean-preview-dsw-science-europe-v1.30.1.zip",
        "clean-workspace-dsw-science-europe-v1.30.1.zip",
        "dsw-science-europe-zh-hant-scaffold-1.30.1.zip",
        "release-notes.md",
    ]
    notes = (release_dir / "release-notes.md").read_text(encoding="utf-8")
    assert "Generated by owner/repo run 123 from commit abc123." in notes
    assert "not finished public translations" in notes


